// ─── XAI Types (Explainable AI) ───────────────────────────────────────────────

/// A single feature's contribution to the anomaly score.
/// Generated by the IsolationForest scorer using deviation-from-baseline heuristics.
/// Based on research: "Explaining Anomaly Detection with SHAP" (IEEE S&P 2023).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FeatureWeight {
    /// Human-readable feature name (matches features.rs index ordering)
    pub feature_name: String,
    /// Raw value fed into the model (already normalized)
    pub feature_value: f32,
    /// Deviation from the normal baseline (0 = normal, 1 = maximum anomaly)
    pub deviation_score: f32,
    /// Normalized importance contribution to anomaly score [0.0, 1.0]
    pub importance: f32,
    /// Direction of anomaly: "above_normal" | "below_normal" | "binary_active"
    pub direction: String,
}

/// Full explainability report attached to ML-generated alerts.
/// Provides human-readable reasoning for why an event was flagged.
/// Complies with GDPR Art. 22 transparency requirements for automated decisions.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct XaiReport {
    /// Model identifier and version
    pub model_version: String,
    /// Raw anomaly score from IsolationForest [0.0, 1.0]
    pub anomaly_score: f32,
    /// Decision threshold used
    pub threshold: f32,
    /// Top-5 features contributing to the anomaly (sorted by importance desc)
    pub top_features: Vec<FeatureWeight>,
    /// Human-readable explanation generated from top features
    pub explanation: String,
    /// Total number of active (non-zero) features
    pub active_feature_count: usize,
    /// ISO-8601 timestamp of report generation
    pub generated_at: String,
}

