impl XaiReport {
    /// Generate an XAI report from a scored feature vector.
    /// Uses deviation-from-baseline heuristic approximation of IsolationForest
    /// decision paths — as described in: Lundberg & Lee, NIPS 2017 (SHAP).
    pub fn generate(features: &[f32], anomaly_score: f32, threshold: f32) -> Self {
        let model_version = "thor-isolation-forest-v3-2026".to_string();
        let mut weights: Vec<FeatureWeight> = Vec::with_capacity(features.len());

        for (i, &val) in features.iter().enumerate() {
            let (name, baseline) = if i < FEATURE_METADATA.len() {
                FEATURE_METADATA[i]
            } else {
                ("unknown_feature", 0.0)
            };

            let deviation = (val - baseline).abs();
            let direction = if val > baseline {
                "above_normal".to_string()
            } else if val < baseline {
                "below_normal".to_string()
            } else {
                "normal".to_string()
            };

            weights.push(FeatureWeight {
                feature_name: name.to_string(),
                feature_value: val,
                deviation_score: deviation,
                importance: 0.0, // filled below
                direction,
            });
        }

        // Normalize importance scores to [0, 1]
        let total_deviation: f32 = weights.iter().map(|w| w.deviation_score).sum();
        if total_deviation > 0.0 {
            for w in weights.iter_mut() {
                w.importance = w.deviation_score / total_deviation;
            }
        }

        // Sort by importance descending, keep top 5
        weights.sort_by(|a, b| b.importance.partial_cmp(&a.importance).unwrap_or(std::cmp::Ordering::Equal));
        let active_feature_count = weights.iter().filter(|w| w.feature_value != 0.0).count();
        let top_features: Vec<FeatureWeight> = weights.into_iter().take(5).collect();

        // Generate natural-language explanation from top features
        let explanation = Self::explain(&top_features, anomaly_score, threshold);

        XaiReport {
            model_version,
            anomaly_score,
            threshold,
            top_features,
            explanation,
            active_feature_count,
            generated_at: chrono::Utc::now().to_rfc3339(),
        }
    }

    fn explain(top_features: &[FeatureWeight], score: f32, threshold: f32) -> String {
        let severity = if score > threshold * 1.5 { "HIGH" }
                       else if score > threshold { "MEDIUM" }
                       else { "LOW" };

        let reasons: Vec<String> = top_features.iter()
            .filter(|f| f.importance > 0.05)
            .map(|f| format!("{} ({:.1}% weight, value={:.3})", 
                             f.feature_name.replace('_', " "), 
                             f.importance * 100.0,
                             f.feature_value))
            .collect();

        if reasons.is_empty() {
            format!("[XAI] Score={:.3} ({} severity). No dominant features identified — global behavioral shift detected.", score, severity)
        } else {
            format!("[XAI] Score={:.3} ({} severity). Primary anomaly drivers: {}. Recommendation: {}.",
                score, severity,
                reasons.join("; "),
                if score > threshold * 1.5 { "Immediate quarantine" } else { "HITL review recommended" }
            )
        }
    }
}

